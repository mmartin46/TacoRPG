/*
A class that calculates the count
of rows and columns of a CSV file.
*/
class Map
{
   private:
      const char *file_name;
      pair<int, int> dimensions;
   public:
      Map(const char *f);
      pair<int, int> calculateMapSize(const char *fileName, const char delim);
      inline int getColumnCount() { return dimensions.first; }
      inline int getRowCount() { return dimensions.second; }
      pair<int, int> getDimensions() { return dimensions; }
};

Map::Map(const char *f)
{
   this->file_name = f;
   if (f == NULL)
   {
      std::cout << "Map(): File Not Found!";
      SDL_Quit();
      exit(1);
   }

   dimensions = this->calculateMapSize(file_name, ',');
   std::cout << dimensions.first << " " << dimensions.second << std::endl;
}



// Returns a pair that caluclates the size of a map.
// \param fileName The name of the file.
// \param delim The delimiter.
pair<int, int> Map::calculateMapSize(const char *fileName, const char delim)
{
   std::ifstream f(fileName);
   pair<int, int> mapSize;

   int c = 0;
   int r = 0;
   std::string line;
   // Counting Rows;
   while (f.peek() != EOF)
   {
      std::getline(f, line);
      ++r;
   }
   mapSize.second = r;

   f.clear(); 
   f.seekg(0, std::ios::beg); // seek to beginning of the file.

   // Counting Columns
   int row_counter = 0;
   int spec_row = 1;
   size_t count;
   while (std::getline(f, line))
   {
      row_counter++;
      if (row_counter == spec_row) {
         count = std::count_if( line.begin(), line.end(), [](char c) { return c == ','; });
         break;
      }
   }
   mapSize.first = ++count;

   return mapSize;
}